file=open("poem,text.txt","w")
file.write("The Attractive And Weak Sand Castle\n A Poem by Anonymous"+
"\nWhose sand castle is that? I think I know."+
"\nIts owner is quite angry though."+
"\nHe was cross like a dark potato."+
"\nI watch him pace. I cry hello."+

"\nHe gives his sand castle a shake,"+
"\nAnd screams I've made a bad mistake."+
"\nThe only other sound's the break,"+
"\nOf distant waves and birds awake."+

"\nThe sand castle is attractive, weak and deep,"+
"\nBut he has promises to keep,"+
"\nTormented with nightmares he never sleeps."+
"\nRevenge is a promise a man should keep."+

"\nHe rises from his cursed bed,"+
"\nWith thoughts of violence in his head,"+
"\nA flash of rage and he sees red."+
"\nWithout a pause I turned and fled."+

"\nWith thanks to the poet, Robert Frost, for the underlying structure.")
file.close()